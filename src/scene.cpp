#include "scene.h"
#include "loader.h" // 引入 OBJ 加载器

// ======================================================================================
// 工厂函数: create_cornell_box()
// ======================================================================================
// 职责:
// 1. 定义场景中的几何体 (三角形网格)。
// 2. 指定每个物体的材质和纹理。
// 3. 将所有物体打包成一个 Scene 对象返回给 Main。
//
// 架构角色: "数据提供者 (Data Provider)"
// ======================================================================================
Scene create_cornell_box() {
    Scene scene;

    // ------------------------------------------------------------------
    // 1. 资源注册
    // ------------------------------------------------------------------
    // 注册所有需要加载的纹理文件。
    // ID 0: "assets/earth.ppm"
    scene.texture_files.push_back("assets/earth.ppm");

    // ------------------------------------------------------------------
    // 2. 材质与颜色预定义
    // ------------------------------------------------------------------
    Vec white = {0.75f, 0.75f, 0.75f};
    Vec red   = {0.75f, 0.25f, 0.25f};
    Vec blue  = {0.25f, 0.25f, 0.75f};
    Vec gold  = {0.8f,  0.6f,  0.2f};
    Vec light = {22.0f, 22.0f, 22.0f}; // 高强度自发光

    // ------------------------------------------------------------------
    // 3. 物体构建 - 使用工厂函数
    // ------------------------------------------------------------------

    // --- 墙壁 (Walls) ---
    // 每面墙用一个三角形覆盖
    // [坐标系]: X(左右), Y(上下), Z(前后)

    // 左墙 (x=1, 红色, 法线朝右)
    scene.objects.push_back(make_triangle(
        {1, 0, 0}, {1, 0, 600}, {1, 600, 0},
        red, DIFF
    ));

    // 右墙 (x=99, 蓝色, 法线朝左)
    scene.objects.push_back(make_triangle(
        {99, 0, 0}, {99, 600, 0}, {99, 0, 600},
        blue, DIFF
    ));

    // 后墙 (z=0, 白色, 法线朝前, 带纹理)
    scene.objects.push_back(make_triangle(
        {-50, 0, 0}, {150, 0, 0}, {50, 300, 0},
        {1,1,1}, DIFF, 0.0f, 0
    ));

    // 前墙 (z=300, 白色, 法线朝后)
    scene.objects.push_back(make_triangle(
        {-50, 0, 300}, {150, 0, 300}, {50, 300, 300},
        white, DIFF
    ));

    // 地板 (y=0, 白色, 法线朝上)
    scene.objects.push_back(make_triangle(
        {-50, 0, 0}, {150, 0, 0}, {50, 0, 600},
        white, DIFF
    ));

    // 天花板 (y=81.6, 白色, 法线朝下)
    scene.objects.push_back(make_triangle(
        {-50, 81.6, 0}, {150, 81.6, 0}, {50, 81.6, 600},
        white, DIFF
    ));

    // --- 外部模型 (Mesh) ---
    // 调用 loader 模块来加载 cube.obj 文件
    load_obj("assets/cube.obj", scene.objects,
             {50.0f, 10.0f, 30.0f}, // 位置: 放在盒子中间偏上
             10.0f,                 // 缩放: 模型原始大小是 -1到1，放大10倍
             gold,                  // 颜色: 金色
             SPEC,                  // 材质: 镜面
             0.9f);

    // --- 光源 (Light) ---
    // 用三角形近似球体光源
    scene.objects.push_back(make_triangle(
        {30, 81.5, 30}, {70, 81.5, 30}, {50, 81.5, 60},
        {0,0,0}, DIFF, 0.0f, -1, light
    ));

    // 打印场景信息
    printf("[Scene] Scene created with %lu objects.\n", scene.objects.size());
    printf("        All geometry: triangles (no spheres/planes)\n");

    return scene;
}
